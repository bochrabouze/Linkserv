    {
        try{

        
        $service =new Service([
            
            
            'servant_id' => 1,
            'application_id'=> 1,
            'Description' => $request->input('Description'),
            
            'Date' => $request->input('Date'),
            'Wage' => $request->input('Wage'),
            'State' => "Accept",
            
        ]);
        $service->save();
    } catch (\Throwable $th) {
               
            
        return response()->json($th->getMessage());
    }
        return response()->json('service created!');
